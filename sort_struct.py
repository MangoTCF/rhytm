def key(x: str):
    sx = x.strip()
    first_letter = sx[4]
    x_type = x[x.find(": ")+2:]
    type_ord = sum([ord(x) for x in x_type])
    return type_ord * 100000 + ord(first_letter) * 1000 + len(sx)

arr = struct.split("\n")
arr.sort(key=key)
